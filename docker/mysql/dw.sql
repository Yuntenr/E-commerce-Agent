CREATE TABLE `dim_date` (
    `date_id` INT NOT NULL COMMENT '主键',
    `year` INT COMMENT '年',
    `quarter` VARCHAR(2) COMMENT '季度',
    `month` INT COMMENT '月份',
    `day` INT COMMENT '第几天',
    PRIMARY KEY (`date_id`)
) ENGINE = InnoDB DEFAULT CHARSET = utf8mb4 COMMENT = '时间维度表';
INSERT INTO dim_date (date_id, year, quarter, month, day)
WITH RECURSIVE date_range AS (
    SELECT DATE('2026-01-01') AS dt
    UNION ALL
    SELECT DATE_ADD(dt, INTERVAL 1 DAY)
    FROM date_range
    WHERE dt < '2026-05-31'
)
SELECT 
    CAST(DATE_FORMAT(dt, '%Y%m%d') AS UNSIGNED) AS date_id,  -- 生成 20260101 格式
    YEAR(dt) AS year,
    CONCAT('Q', QUARTER(dt)) AS quarter,  -- 生成 'Q1', 'Q2' 格式
    MONTH(dt) AS month,
    DAY(dt) AS day
FROM date_range
ORDER BY dt;

